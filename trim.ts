//TrimLeft & Trim (Удаление пробелов)
//Реализуйте тип TrimLeft<S>, который принимает строку и удаляет все пробельные символы только с начала строки.

//Реализуйте тип Trim<S>, который удаляет пробельные символы с обоих концов строки.
// Под "пробельными символами" подразумеваются: пробел ' ', перенос строки '\n' и табуляция '\t'.

// Примеры:
// // TrimLeft
// type A = TrimLeft<'  Hello World  '> // 'Hello World  '

// // Trim
// type B = Trim<'  Hello World  '>     // 'Hello World'

type TrimLeft<S extends string> = S extends `${" " | "\n" | "\t"}${infer R}` ? TrimLeft<R> : S;

type Trim<S extends string> = S extends `${" " | "\n" | "\t"}${infer R}` | `${infer R}${" " | "\n" | "\t"}`
    ? Trim<R>
    : S;

// Как это работает:

// ${Space}${infer R}: Шаблон проверяет, начинается ли строка с одного из символов Space.
// Если да, то оставшаяся часть строки ("хвост") сохраняется в переменную R.
// TrimLeft<R> (Рекурсия): Если пробел найден, мы "отрезаем" его и снова вызываем TrimLeft для хвоста R. Это повторяется, пока все пробелы в начале не исчезнут.
// : S: Если пробелов в начале больше нет, возвращаем строку как есть.

// Полезные концепции
// Template Literal Types: Шаблоны строк ${Start}${End}.
// Recursive Conditional Types: Рекурсия для обработки последовательностей.
// Inference (infer) in Templates: Вытаскивание подстрок.
// Union Types in extends: Возможность матчить несколько шаблонов одновременно.
